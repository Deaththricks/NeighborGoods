    private void jButtonSumitItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSumitItemMouseClicked
                                                        
    String itemName = jTextItemName.getText().trim();
    String itemDesc = jTextDesc.getText().trim();
    String itemPrice = jTextFullPrice.getText().trim();
    String itemDeposit = jTextDepositPrice.getText().trim();
    String categoryName = (String) cmbCategory.getSelectedItem();

    if (itemName.isEmpty() || itemDesc.isEmpty() || itemPrice.isEmpty() || itemDeposit.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Semua field wajib diisi!");
        return;
    }

    try {
        double price = Double.parseDouble(itemPrice);
        double deposit = Double.parseDouble(itemDeposit);

        Connection conn = DatabaseConnection.connect();

        // 1. LOOK UP THE CATEGORY_ID BASED ON THE NAME SELECTED
        String getCatIdSql = "SELECT category_id FROM category WHERE category_name = ?";
        PreparedStatement catPstmt = conn.prepareStatement(getCatIdSql);
        catPstmt.setString(1, categoryName);
        ResultSet catRs = catPstmt.executeQuery();
        
        int categoryId = 0;
        if (catRs.next()) {
            categoryId = catRs.getInt("category_id");
        }
        catRs.close();
        catPstmt.close();

        // If for some reason we couldn't map the category id, halt.
        if (categoryId == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Kategori tidak valid!");
            return;
        }

        // 2. INSERT INTO THE ITEM TABLE MATCHING YOUR EXACT SCHEMA
        String sql = "INSERT INTO item "
                + "(user_id, category_id, item_name, item_desc, "
                + "item_price_per_day, item_deposit, item_condition, "
                + "item_status, item_image) "
                + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)"; // 9 parameters

        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setInt(1, UserSession.getUserId());
        pstmt.setInt(2, categoryId); // Matches your int category_id column!
        pstmt.setString(3, itemName);
        pstmt.setString(4, itemDesc);
        pstmt.setDouble(5, price);
        pstmt.setDouble(6, deposit);
        pstmt.setString(7, "Baik");
        pstmt.setString(8, "Available"); // Matches your enum('Available', 'Rented') precisely!
        pstmt.setString(9, ""); // item_image empty string

        pstmt.executeUpdate();

        javax.swing.JOptionPane.showMessageDialog(this, "Item berhasil ditambahkan!");
        DashboardPannel defaultPannel = new DashboardPannel();
        
        // Clear out text fields
        jTextItemName.setText("");
        jTextDesc.setText("");
        jTextFullPrice.setText("");
        jTextDepositPrice.setText("");
        
        pstmt.close();
        conn.close();

    } catch (NumberFormatException nfe) {
        javax.swing.JOptionPane.showMessageDialog(this, "Harga dan Deposit harus berupa angka!");
    } catch (SQLException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error Database: " + e.getMessage());
    }

    }//GEN-LAST:event_jButtonSumitItemMouseClicked


